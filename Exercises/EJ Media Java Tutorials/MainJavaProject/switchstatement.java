class switchstatement {
    public static void main (String[] args) {
        int pontoi;
        pontoi = 29999;
        switch (pontoi) {
            case 2:
            System.out.println ("mono 2 pontous, ta phges xalia");
            break;
            case 4:
            System.out.println ("mono 4 pontous, mporeis kai poly kalytera");
            break;
            case 30000:
            System.out.println ("Ayto den paizei");
            break;
           case 10:
           System.out.println ("eskises");
           break;
           default:
           System.out.println ("den mporeis na exeis tosous pontous");
           break;
        
        }
        
        
    }
    
}    
    